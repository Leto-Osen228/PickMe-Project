#!/usr/bin/bash

# Подсчитать количество строк содержимого файлов в директории treecko5, результат записать в файл в директории /tmp, подавить вывод ошибок доступа
find ./trecho5 -type f -exec cat {} + 2>/dev/null | wc -l > /tmp/treecko5_lines.txt

# Вывести три первых элемента рекурсивного списка имен и атрибутов файлов в директории lab0, список отсортировать по имени z->a, добавить вывод ошибок доступа в стандартный поток вывода
ls -lR ./lab0 2>&1 | sort -r | head -n 3

# Рекурсивно вывести содержимое файлов с номерами строк из директории lab0, имя которых начинается на 't', строки отсортировать по имени a->z, добавить вывод ошибок доступа в стандартный поток вывода
find ./lab0 -type f -name 't*' -print 2>&1 | sort | while read f; do nl -ba "$f" 2>&1; done

# Вывести рекурсивно список имен и атрибутов файлов в директории lab0, начинающихся на символ 'l', список отсортировать по убыванию даты модификации файла, добавить вывод ошибок доступа в стандартный поток вывода
find ./lab0 -name 'l*' -exec ls -lt {} + 2>&1

# Вывести рекурсивно список имен файлов в директории treecko5, список отсортировать по имени z->a, подавить вывод ошибок доступа
find ./trecho5 -type f -print 2>/dev/null | sort -r

# Подсчитать количество символов содержимого файла klang6, результат записать в файл в директории /tmp, ошибки доступа перенаправить в файл в директории /tmp
wc -m ./klang6 > /tmp/klang6_chars.txt 2>/tmp/klang6_err.txt